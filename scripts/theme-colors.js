/** 返回全部主题 ID 列表（不含别名） */
const VALID_THEME_IDS = Object.keys(THEME_COLORS).filter(k => !THEME_COLORS[k].aliasOf);

/** 根据 key 获取主题颜色（包含 cover 字段），找不到返回 cyberpunk */
function getThemeColors(key) {
  return THEME_COLORS[key] || THEME_COLORS['cyberpunk'];
}

/** 返回某主题的 Remotion 兼容动画配置（glow等） */
function getThemeAnimationConfig(key) {
  const tc = getThemeColors(key);
  return {
    spring: tc.spring || { damping: 12, stiffness: 180, mass: 1 },
    fade: tc.fade || { duration: 12 },
    slide: tc.slide || { duration: 20 },
    scale: tc.scale || { from: 0.85, to: 1 },
    glow: tc.glow,
    particle: tc.particle || { speed: 0.8, count: 1 },
    special: tc.special || [],
  };
}

